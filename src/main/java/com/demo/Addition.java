package com.demo;

public class Addition {
    int a;
    int b;

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public Addition(int a, int b) {
        this.a = a;
        this.b = b;
    }
    public int add()
    {
        return a+b;
    }
}
